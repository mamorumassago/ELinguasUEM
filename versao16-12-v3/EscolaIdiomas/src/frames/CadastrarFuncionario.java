    public boolean funcionarioNaoExiste(Funcionario fc){
        try{
            ControladorDePersistencia con = new ControladorDePersistencia();
            ArrayList<Funcionario> lf = con.carregaFuncionario();
            for(Funcionario f1:lf){
                if(f1.getEmail().toUpperCase().equals(fc.getEmail().toUpperCase())){
                    return false;
                }
            }
        }
        catch(Exception ex){
            
        }
        return true;
    }
    
    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        Funcionario funcionario = null;
        String Nome, Endereco, Telefone, Email, Senha, ConfSenha;
        int Status;
        double Salario;
        
        try{
            
            Nome = this.jTextField3.getText().toString();
            if (Nome.length() == 0){
                JOptionPane.showMessageDialog(this,"Insira um nome.","Warning",JOptionPane.WARNING_MESSAGE);
                return;
            }
            Endereco = this.jTextField2.getText().toString();
            if (Endereco.length() == 0){
                JOptionPane.showMessageDialog(this,"Insira um endereco.","Warning",JOptionPane.WARNING_MESSAGE);
                return;
            }
            Telefone = this.jTextField4.getText().toString();
            if (Telefone.length() == 0){
                JOptionPane.showMessageDialog(this,"Insira um telefone válido.","Warning",JOptionPane.WARNING_MESSAGE);
                return;
            }
            try{
                if (this.jTextField5.getText().toString() == ""){
                    JOptionPane.showMessageDialog(this,"Insira um Salário.","Warning",JOptionPane.WARNING_MESSAGE);
                    return;
                }
                else{
                    Salario = Double.parseDouble(this.jTextField5.getText().toString());
                }
            }
            catch(Exception exs){
                JOptionPane.showMessageDialog(this,"Insira um Salário válido.","Warning",JOptionPane.WARNING_MESSAGE);
                    return;
            }
            Email = this.jTextField1.getText().toString();
            if (Email.length() == 0){
                JOptionPane.showMessageDialog(this,"Insira um Email válido.","Warning",JOptionPane.WARNING_MESSAGE);
                return;
            }
            if(emailUsado(Email)){
                JOptionPane.showMessageDialog(this,"Email já utilizado.","Warning",JOptionPane.WARNING_MESSAGE);
                return;
            }
            Senha = this.jTextField6.getText().toString();
            ConfSenha = this.jTextField7.getText().toString();
            if(!Senha.equals(ConfSenha)){
                JOptionPane.showMessageDialog(this,"As senhas não sao iguais.","Warning",JOptionPane.WARNING_MESSAGE);
                return;
            }
            Status = 1;
            
            funcionario = new Funcionario(Nome, Endereco, Telefone, Salario, Email, Senha, tipo, Status);
            
            if(funcionarioNaoExiste(funcionario)){
                ControladorDePersistencia per = new ControladorDePersistencia();
                boolean b = per.cadastraFuncionario(funcionario);

                if(b){
                    JOptionPane.showMessageDialog(this,"Funcionário Cadastrado com Sucesso.","Mensagem",JOptionPane.INFORMATION_MESSAGE);
                }
                else{
                    JOptionPane.showMessageDialog(this,"Erro ao cadastrar Funcionario.","Warning",JOptionPane.WARNING_MESSAGE);
                }
            }
            else{
                JOptionPane.showMessageDialog(this,"Funcionário já existe");
                return;
            }
            
            
        }
        catch(Exception ex){
            
        }
        
        
        mp.setEnabled(true);
        dispose();
    }//GEN-LAST:event_jButton2ActionPerformed

