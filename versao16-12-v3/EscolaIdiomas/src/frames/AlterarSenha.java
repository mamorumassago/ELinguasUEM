    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        
        try{
            ControladorDePersistencia c = new ControladorDePersistencia();
            ArrayList<Funcionario> listaFunc= c.carregaFuncionario();
            String email = this.jTextField1.getText().toString();
            if(email.length() == 0){
                JOptionPane.showMessageDialog(this,"Insira um email","Error",JOptionPane.ERROR_MESSAGE);
                return;
            }
            
            boolean emailExiste = false;
            for (Funcionario f:listaFunc){
                if(f.getEmail().equals(email)){
                    if(f.getTipo() > 2){
                        JOptionPane.showMessageDialog(this,"Este usuario não tem permissão para alterar senha","Error",JOptionPane.ERROR_MESSAGE);
                        return;
                    }
                    emailExiste = true;                        
                }
            }
            if(!emailExiste){
                JOptionPane.showMessageDialog(this,"Email não cadastrado","Error",JOptionPane.ERROR_MESSAGE);
                return;
            }
            String senha = this.jTextField2.getText().toString();
            if(senha.length() == 0){
                JOptionPane.showMessageDialog(this,"Insira uma senha","Error",JOptionPane.ERROR_MESSAGE);
                return;
            }
            
            Funcionario newF = new Funcionario();
            boolean existe = false;
            for (Funcionario f:listaFunc){
                if(f.getEmail().equals(email)){
                    //System.out.println("-"+f.getSenha()+"-"+senha+"-");
                    if(f.getSenha().equals(senha)){
                        existe = true;
                        newF.clonarFuncionario(f);
                    }
                }
            }
            if(!existe){
                JOptionPane.showMessageDialog(this,"Combinação inválida","Error",JOptionPane.ERROR_MESSAGE);
                return;
            }
            String novaSenha = this.jTextField3.getText().toString();
            if(novaSenha.length() == 0){
                JOptionPane.showMessageDialog(this,"Insira uma senha válida","Error",JOptionPane.ERROR_MESSAGE);
                return;
            }
            String repeteSenha = this.jTextField4.getText().toString();
            if(repeteSenha.length() == 0){
                JOptionPane.showMessageDialog(this,"Insira uma senha válida","Error",JOptionPane.ERROR_MESSAGE);
                return;
            }
            if (novaSenha.equals(repeteSenha)){
                newF.setSenha(novaSenha);
                ControladorDePersistencia c2 = new ControladorDePersistencia();
                boolean resultado = c2.alteraSenha(newF);
                if(resultado)
                    JOptionPane.showMessageDialog(this,"Senha alterada");
                else
                    JOptionPane.showMessageDialog(this,"Erro ao alterar dados","Error",JOptionPane.ERROR_MESSAGE);
                
                return;
                
            }
            else{
                JOptionPane.showMessageDialog(this,"As senhas digitadas não são iguais","Error",JOptionPane.ERROR_MESSAGE);
                return;
            }
            
        }
        catch(Exception ex){
            ex.printStackTrace();
        }
        
        
    }//GEN-LAST:event_jButton1ActionPerformed

