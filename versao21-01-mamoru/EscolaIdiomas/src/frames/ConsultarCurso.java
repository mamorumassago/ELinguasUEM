    public void buscar(){
        this.carregaBusca();
        this.listarAtivos();
    }
    
    public void carregaBusca(){
        String nomeBusca = formatar(jTextField3.getText().toString());
        listaCurso.clear();
        for(Curso c:listaCursoAtivo){
            if(formatar(c.getNome().toUpperCase()).contains(formatar(nomeBusca.toUpperCase()))){
                Curso c1 = new Curso();
                c1.clonarCurso(c);
                listaCurso.add(c1);
            }
        }
        
    }
    
    
    public String formatar(String s){
        String saida = "";
        for (int i=0;i<s.length();i++){
            if(s.charAt(i) != ' '){
                saida += s.charAt(i);
            }
        }
        return saida;
    }
    
    public void reload(){
        this.carregaCurso();
        this.listarAtivos();
    }
    
    private void carregaCurso(){
        listaCursoAtivo = new ArrayList<>();
        listaCurso = new ArrayList<>();
        try{
            ControladorDePersistencia con = new ControladorDePersistencia();            
            ArrayList<Curso> lstC = con.carregaCurso();   
            for (Curso c:lstC){
                if (c.getStatus() == 1){
                    listaCursoAtivo.add(c);
                    this.listaCurso.add(c);

                    Collections.sort(listaCursoAtivo, Curso.CursoNameComparator);
                    Collections.sort(listaCurso, Curso.CursoNameComparator);
                }
                
            }
        }
        catch(Exception ex){
            ex.printStackTrace();
        }
    }
    
    public void listarAtivos(){
        try{
            DefaultTableModel tabelaP = (DefaultTableModel)
            jTable1.getModel();
            tabelaP.setNumRows(0);
            for (Curso c:listaCurso){
                tabelaP.addRow(new Object[]{
                                c.getNome(),
                                c.getCarga(),
                                c.getMensalidade(),
                                
                                });

            }
            
        } catch (Exception e){
        }
    } 
