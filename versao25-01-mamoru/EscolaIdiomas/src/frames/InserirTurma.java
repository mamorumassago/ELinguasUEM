    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        
        String nome, emailprofessor, horarios;
        int id, idcurso, idnivel, vagas, status;
        try{
            nome = jTextField3.getText().toString();
            if(nome.length() == 0){
                JOptionPane.showMessageDialog(this,"Insira um nome.","Warning",JOptionPane.WARNING_MESSAGE);
                return;
            }
            if(jTextField2.getText().length() == 0){
                JOptionPane.showMessageDialog(this,"Selecione um curso","Warning",JOptionPane.WARNING_MESSAGE);
                return;
            }
            idcurso = curso.getId();
            
            //selecionaNomeCbox();
            try{
                idnivel = listNivel.get(jComboBox1.getSelectedIndex()).getId();
            }
            catch(Exception excbox){
                JOptionPane.showMessageDialog(this,"Selecione um nivel","Warning",JOptionPane.WARNING_MESSAGE);
                return;
            }
            
            if(jTextField5.getText().length() == 0){
                JOptionPane.showMessageDialog(this,"Selecione um professor","Warning",JOptionPane.WARNING_MESSAGE);
                return;
            }
            emailprofessor = funcionario.getEmail();
            try{
                vagas = Integer.parseInt(jTextField6.getText().toString());
            }
            catch(Exception exvaga){
                JOptionPane.showMessageDialog(this,"Insira uma quantidade de vagas válido","Warning",JOptionPane.WARNING_MESSAGE);
                return;
            }
            horarios = jTextArea1.getText().toString();
            if(horarios.length() == 0){
                JOptionPane.showMessageDialog(this,"Insira horários","Warning",JOptionPane.WARNING_MESSAGE);
                return;
            }

            
            if(selecionado == 1){
                id = 0;
                status = 1;
                Turma t = new Turma(id, nome, idcurso, idnivel, emailprofessor, vagas, 0,horarios, status);
                ControladorDePersistencia con = new ControladorDePersistencia();

                boolean resultado = con.cadastraTurma(t);
                if(resultado){
                    JOptionPane.showMessageDialog(this,"Turma cadastrado com sucesso","Warning",JOptionPane.WARNING_MESSAGE);
                    mp.setEnabled(true);
                    dispose();
                }
                else{
                    JOptionPane.showMessageDialog(this,"Erro ao cadastrar turma","Warning",JOptionPane.WARNING_MESSAGE);
                    return;
                }
            }
            if(selecionado == 2){
                id = turma.getId();
                status = turma.getStatus();
                Turma t = new Turma(id, nome, idcurso, idnivel, emailprofessor, vagas, 0, horarios, status);
                ControladorDePersistencia con = new ControladorDePersistencia();

                boolean resultado = con.alteraTurma(t);
                if(resultado){
                    JOptionPane.showMessageDialog(this,"Turma alterado com sucesso","Warning",JOptionPane.WARNING_MESSAGE);
                    ct.reload();
                    ct.setEnabled(true);
                    dispose();
                }
                else{
                    JOptionPane.showMessageDialog(this,"Erro ao alterar turma","Warning",JOptionPane.WARNING_MESSAGE);
                    return;
                }
            }

        }
        catch(Exception ex){

        }
        
        dispose();
    }//GEN-LAST:event_jButton2ActionPerformed

