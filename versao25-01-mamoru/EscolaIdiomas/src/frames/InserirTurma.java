    public void setNivel(){
        try{
            ArrayList<Nivel> listNivel = new ArrayList<Nivel>();
            ControladorDePersistencia con = new ControladorDePersistencia();
            listNivel = con.carregaNivel();
            nivel = new Nivel();
            for(Nivel n:listNivel){
//                System.out.println(n.getIdcurso()+"-"+curso.getId());
                if(n.getIdcurso() == curso.getId()){
                    nivel.clonarNivel(n);
                    return;
                }
            }
        }
        catch(Exception ex){
            
        }
    }
     
